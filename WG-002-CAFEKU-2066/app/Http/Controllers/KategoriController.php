<?php

namespace App\Http\Controllers;

use App\Models\Kategori;
use Illuminate\Http\Request;

class KategoriController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data = Kategori::all(); //pendeklarasian $data yang berisi data dari model kategori

        return view('kategori.tampil', compact('data')); //menampilkan ke page tampil dengan membawa data
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('kategori.tambah');//menampilkan ke page tambah di folder kategori
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Kategori::create($request->all()); //memakai fungsi create dari laravel dengan cara $request all yang artinya mengambil semua data

        return redirect('kategori');//pendeklarasian route 'kategori' untuk dapat dipanggil di route web
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Kategori  $kategori
     * @return \Illuminate\Http\Response
     */
    public function show(Kategori $kategori)
    {

    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Kategori  $kategori
     * @return \Illuminate\Http\Response
     */
    public function edit(Kategori $kategori)
    {
        return view('kategori.edit', compact('data')); //menampilkan ke page edit di folder kategori
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Kategori  $kategori
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Kategori $kategori)
    {
        Kategori::update($request->all()); //menggunakan fungsi update dari laravel dengan request
        return redirect('kategori'); // diarahkan ke kategori agar bisa dirouting
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Kategori  $kategori
     * @return \Illuminate\Http\Response
     */
    public function destroy(Kategori $kategori)
    {
        $kategori->delete(); //pemanggilan fungsi delete
        return redirect('kategori'); // diarahkan ke kategori untuk routingnya
    }
}
